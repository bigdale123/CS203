package dylcal13_Hamidc_HW5;

public class Book_info {
	String name;
	String Author;
	String publisher;
	int page;
	int stock;


	public Book_info(String name, String Author, int page, String publisher, int stock) {
		this.name = name;
		this.Author = Author;
		this.page = page;
		this.publisher = publisher;
		this.stock = stock;
		
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method getName: 																				    //																													
//@param this.name																				    //
//@returns String name																		   		//				
//"Hunger Games"																					//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getName() {

		return this.name;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method setName: 																					//																													
//@param String name																				//
//@returns this.name																		   		//				
//"Hunger Games"																					//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setName(String name) {

		this.name = name;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method getAuthor: 																				//																													
//@param this.Author																				//
//@returns String Author																		   	//				
//"Suzanne Collins"																					//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getAuthor() {

		return this.Author;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method setAuthor: 																				//																													
//@param String Author																				//
//@returns this.Author																		   		//				
//"Suzanne Collins"																					//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setAuthor(String Author) {

		this.Author = Author;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method getPageNumber: 																			//																													
//@param this.page																					//
//@returns Integer page																		   		//				
//"374"																								//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public int getPageNumber() {

		return this.page;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method setPageNumber: 																			//																													
//@param String page																				//
//@returns this.page																		   		//				
//"374"																								//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setPageNumber(int num) {

		this.page = num;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method setPub: 																				    //																													
//@param String publisher																			//
//@returns this.publisher																		   	//				
//"Scholastic"																						//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getPub() {

		return this.publisher;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method getPub: 																				    //																													
//@param this.publisher																				//
//@returns String publisher																		   	//				
//"Scholastic"																						//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setPub(String publisher) {

		this.publisher = publisher;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method getStock: 																					//																													
//@param this.stock																					//
//@returns Integer stock																		   	//				
//"5"																								//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public int getStock() {

		return this.stock;
	}
//////////////////////////////////////////////////////////////////////////////////////////////////////
//method setStock: 																					//																													
//@param String stock																				//
//@returns this.stock																		   		//				
//"5"																								//
//////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setStock(int stock) {

		this.stock = stock;
	}
	public String toString() {

		String out = "Book name: " + this.getName() +"Author name: " + this.getAuthor() + "Page number: " + this.getPageNumber() + "Publisher of Book: " + this.getPub()+ " In Stock: " + this.getStock();
		return out;
	}


}


